def main():
    # Tipos de números inteiros de dados:
    idade = 21
    print(idade)
    print(10 + 10)
    print(5.5 + 4)
    print(5 - 10)
    print(4 / 2)
    print(5 * 5)
    print(5 % 5)

    # Tipos de números fluturantes de dados:
    salario = 1500.00
    print(round(salario, 2))
    print(5.234 + 24.414)

    # Tipos de caractéres:
    variavelChar = "A" # char
    nome = "Paulo Ricardo Proença" # string
    print(variavelChar)
    print(nome)

    # Tipos de dados booleanos (verdadeiro ou falso):
    maiorDeIdade = True
    ricoSimOuNao = False
    print(maiorDeIdade)
    print(ricoSimOuNao)

if __name__ == '__main__':
    main()